let a = 6;
let b = '3';

if (a > b) {
    console.log(`${a} je vece od ${b}`);
}
else {
    console.log(`${a} je manje od ${b}`);
}

console.log("Command after IF");

if (a == b) {
    console.log(`${a} je jednako(vrednost) ${b}`);
}
else {
    console.log(`${a} nije jednako ${b}`);
}

if (a === b) {
    console.log(`${a} je jednako(tip, vrednost) ${b}`);
}
else {
    console.log(`${a} nije jednako(tip, vrednost) ${b}`);
}



























